package com.NonPrimitiveArray.Comparable;

public class Emp2 {
	String name;
	int age;
	int id;
	public Emp2(String name, int age, int id) {
		this.name = name;
		this.age = age;
		this.id = id;
	}
	@Override
	public String toString() {
		return "Emp2 [name=" + name + ", age=" + age + ", id=" + id + "]";
	}
	
	

}
